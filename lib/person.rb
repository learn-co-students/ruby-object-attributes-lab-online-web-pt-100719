# Create our Person class
class Person

  # Create our writer method
  def name=(name)
    @name = name
  end

  # Create our reader methods
  def name
    @name
  end

  # Create a second writer method for job
  def job=(job)
    @job = job
  end

  # Create a second reader method of job
  def job
    @job
  end

end
